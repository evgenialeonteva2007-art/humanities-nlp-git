from collections import Counter
import re

# 1. Создаем набор стоп-слов (для русского языка пример небольшой)
# В реальных проектах используют готовые библиотеки, например, nltk или stop-words
STOP_WORDS = {
    "это", "и", "в", "во", "не", "что", "он", "на", "я", "с", "со",
    "как", "а", "то", "все", "она", "так", "его", "но", "да", "ты",
    "к", "у", "же", "вы", "за", "бы", "по", "только", "ее", "мне",
    "было", "вот", "от", "меня", "еще", "нет", "о", "из", "ему",
    "теперь", "когда", "даже", "ну", "вдруг", "ли", "если", "уже",
    "или", "ни", "быть", "был", "него", "до", "вас", "нибудь", "опять"
}

def count_words(text: str, top_n: int = 5) -> dict:
    """
    Считает частоту слов, исключая стоп-слова и пунктуацию.
    
    Args:
        text: Входной текст.
        top_n: Сколько топ-слов вернуть (по умолчанию 5).
    
    Returns:
        Словарь {слово: количество}.
    """
    # 2. Приводим к нижнему регистру и находим все слова (буквы, цифры, подчеркивания)
    # \b - граница слова, \w+ - одно или более буквенно-цифровых символов
    words = re.findall(r'\b\w+\b', text.lower())
    
    # 3. Фильтрация: оставляем только те слова, которых НЕТ в стоп-словах
    # Также можно отфильтровать слишком короткие слова (например, меньше 2 букв)
    filtered_words = [
        word for word in words 
        if word not in STOP_WORDS and len(word) > 2
    ]
    
    # 4. Считаем частоту уже отфильтрованных слов
    if not filtered_words:
        return {}
        
    return dict(Counter(filtered_words).most_common(top_n))

if __name__ == "__main__":
    sample_text = (
        "Git это система контроля версий. Git помогает командам работать вместе. "
        "Это важно, потому что это позволяет отслеживать изменения в коде. "
        "Система Git очень популярна среди разработчиков."
    )
    print("=== Анализ текста без стоп-слов ===")
    result = count_words(sample_text)
    for word, count in result.items():
        print(f"{word}: {count}")
